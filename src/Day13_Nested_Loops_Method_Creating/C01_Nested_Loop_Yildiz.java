package Day13_Nested_Loops_Method_Creating;

public class C01_Nested_Loop_Yildiz {

    /*
         *            * * * * * * * *        * * * * * * * *
         * *          * * * * * * * *        * * * * * *
         * * *        * * * * * * * *        * * * *
         * * * *      * * * * * * * *        * *
         */
        public static void main(String[] args) {
            for (int j = 1; j <=4 ; j++) {
                for (int i = 1; i <=8 ; i++) {
                    System.out.print("* ");
                }
                System.out.println();
            }
        }
    }


